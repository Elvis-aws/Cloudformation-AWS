ClodFormation Deploy
********************
    - Once your template is ready, you can begin the deployment process to create the resources you have defined in 
      the template in your actual AWS environment
    - There are multiple ways to deploy a CloudFormation template
        ***************
        The AWS console
        ***************
            - If your template is a text file stored on your local computer, the easiest way to deploy it is to log 
              into the AWS CloudFormation console
        ***********************
        CloudFormation Designer
        ***********************
            - If you create your template in CloudFormation Designer, you can deploy it directly from There
        ***********
        The AWS CLI
        ***********
            - Using the AWS CLI tool, you can use the aws cloudformation deploy command to deploy a template