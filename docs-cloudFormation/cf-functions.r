
*******************
Intrinsic Functions
*******************
    Fn::FindInMap
        - Returns the value of the specified key
    Fn::GetAtt
        - Returns the list of attributes of the specified resource
    Fn::GetAZs
        - Returns the availability zones
    Fn::If
        - Condition
    Fn::ImportValue
        - Import values from another stack by referencing the export name
    Fn::Join
        - Join strings
          !Join [ delimiter, [ comma-delimited list of values ] ]
          !Join [ ":", [ a, b, c ] ]
    Fn::Sub
        - To substitute values
          !Sub String
    FN::Ref
        - !Ref 
        - Returns the value of the parameter or the ID of the resource specified