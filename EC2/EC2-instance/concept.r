EC2
*****
    - Consist of the following capabilities
        - ALB
        - ASG
        - EBS
    - Operating systems 
        - Linux
        - Windows
        - MacOS
    - Bootstrap script
        - To configure instance before lunching it 
        - Script is only run once during initialization 
        - To do boot tasks e.g install updates, download and install software 
        - Run by the root user 
    - IP
        - Changes upon stop start (public)
        - Does not change (private)