

Amazon EC2 Auto Scaling
    Amazon EC2 Auto Scaling helps you ensure that you have the correct number of Amazon EC2 instances available 
    to handle the load for your application. You create collections of EC2 instances, called Auto Scaling groups. 
    You can specify the minimum number of instances in each Auto Scaling group, and Amazon EC2 Auto Scaling 
    ensures that your group never goes below this size. You can specify the maximum number of instances in each 
    Auto Scaling group, and Amazon EC2 Auto Scaling ensures that your group never goes above this size. If you 
    specify the desired capacity, either when you create the group or at any time thereafter, Amazon EC2 Auto 
    Scaling ensures that your group has this many instances. If you specify scaling policies, then Amazon EC2 
    Auto Scaling can launch or terminate instances as demand on your application increases or decreases.